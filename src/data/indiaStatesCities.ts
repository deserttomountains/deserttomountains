export const getStates = () => {
  return indiaStatesAndCities.map(state => ({
    name: state.name,
    code: state.code
  }));
};

export const getCitiesByState = (stateName: string) => {
  const state = indiaStatesAndCities.find(s => s.name === stateName);
  return state ? state.cities : [];
};

export const getStateByCode = (code: string) => {
  return indiaStatesAndCities.find(s => s.code === code);
};

export const getCitiesByStateCode = (stateCode: string) => {
  const state = indiaStatesAndCities.find(s => s.code === stateCode);
  return state ? state.cities : [];
}; 